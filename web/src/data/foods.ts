export function getFoodById(id: string): Food | undefined {
  return foods.find((f) => f.id === id);
}

export function getFoodsByCategory(category: FoodCategory): Food[] {
  return foods.filter((f) => f.category === category);
}

export function searchFoods(keyword: string): Food[] {
  const lowerKeyword = keyword.toLowerCase();
  return foods.filter(
    (f) =>
      f.name.toLowerCase().includes(lowerKeyword) ||
      f.id.toLowerCase().includes(lowerKeyword)
  );
}

export function calculateFoodHpRestore(
  food: Food,
  playerMaxHp: number
): number {
  const baseRestore = food.hpRestore;
  const maxRestore = Math.floor(playerMaxHp * 0.3);
  return Math.min(baseRestore, maxRestore);
}
